/**
 * Key codes enum.
 */
export enum KeyCodes {
    /**
     * Backspace key.
     */
	BACKSPACE = 8,

    /**
     * Tab key.
     */
	TAB = 9,

    /**
     * Enter key.
     */
	ENTER = 13,

    /**
     * Escape key.
     */
	ESCAPE = 27,

    /**
     * Left key.
     */
	LEFT = 37,

    /**
     * Up key.
     */
	UP = 38,

    /**
     * Right key.
     */
	RIGHT = 39,

    /**
     * Down key.
     */
	DOWN = 40,

    /**
     * Delete key.
     */
	DELETE = 46,

    /**
     * Space key.
     */
	SPACE = 32,

    /**
     * Page up key.
     */
	PAGE_UP = 33,

    /**
     * Page down key.
     */
	PAGE_DOWN = 34,
}