/**
 * Defines IDs of standard dialog buttons.
 *
 * @group Dialog Framework
 */
export enum StandardDialogButtonId {

	/**
	 * The ID of the "Ok" button.
	 */
	Ok = 'ok',

	/**
	 * The ID of the "Yes" button.
	 */
	Yes = 'yes',

	/**
	 * The ID of the "No" button.
	 */
	No = 'no',

	/**
	 * The ID of the "Ignore" button.
	 */
	Ignore = 'ignore',

	/**
	 * The ID of the "Cancel" button.
	 */
	Cancel = 'cancel',

	/**
	 * The ID of the "Retry" button.
	 */
	Retry = 'retry',
}
